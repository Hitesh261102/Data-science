# 5 String methods
# 1. upper() : this method is used to convert the string to uppercase.
abc = "pilani"
print(abc.upper())

# 2. lower() : this method is used to convert the string to lowercase.
ab = "PILANI"
print(ab.lower())

# 3. replace() : this method is used to replace a word to another word in a String.
a = "Hello world"
print(a.replace("world","Hitesh"))

# 4. strip() : this method is used to remove the white spaces of the string in starting and ending.
s = "   hello   "
print(s.strip())

# 5. title() : this method the converts the first letter of the word to capital
print(abc.title())

